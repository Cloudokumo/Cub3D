#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dup;
	int		i;
	int		n;

	n = ft_strlen(s);
	dup = (char *)malloc((n + 1) * sizeof(char));
	if (dup == NULL)
		return (0);
	i = 0;
	while (s[i])
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
